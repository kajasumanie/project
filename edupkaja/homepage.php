<p style='font-size:14px' align="justify"><strong>Evolving Northern Education Data Management Practice</strong></p>

<p style='font-size:12px' align="justify">Data and information are very vital factors of efficient education planning, development and management processes.
The earlier practice of collecting data through printed forms had its drawbacks such as delays, data losses and time consuming processing
methods. Therefore a standalone digital data base was designed and the data collection was administered through compact discs which were
distributed among Education Zones to enter their raw data at their offices and return in a specified time interval. Even though this
method was advantageous than the earlier methods it still had several shortfalls such as technical difficulties in handling, different
approaches of raw data collection from schools and processing delays. Considering these challenges it was attempted to maintain an
